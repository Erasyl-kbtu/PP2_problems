import pygame  # Импортируем библиотеку pygame для создания игр
import sys     # Импортируем модуль sys для выхода из программы

pygame.init()  # Инициализируем все модули pygame (графика, звук, управление)
WIDTH, HEIGHT = 1200, 800  # Устанавливаем ширину и высоту окна игры
screen = pygame.display.set_mode((WIDTH, HEIGHT))  # Создаем окно игры с заданными размерами
clock = pygame.time.Clock()  # Создаем объект часов для контроля FPS (кадров в секунду)

# Класс бойца
class Fighter:  # Объявляем класс Fighter (шаблон для создания бойцов)
    def __init__(self, x, color, left_key, right_key, attack_key):  # Конструктор класса, вызывается при создании бойца
        self.x = x  # Сохраняем координату X бойца (горизонтальное положение)
        self.y = HEIGHT - 150  # Устанавливаем Y координату (от низа экрана отступаем 150 пикселей)
        self.width = 60  # Устанавливаем ширину бойца (80 пикселей)
        self.height = 100  # Устанавливаем высоту бойца (120 пикселей)
        self.color = color  # Сохраняем цвет бойца (будет передан при создании)
        self.health = 100  # Устанавливаем начальное здоровье бойца (100 единиц)
        self.speed = 5  # Устанавливаем скорость движения (5 пикселей за кадр)
        self.attack_cooldown = 0  # Счетчик перезарядки атаки (0 - можно атаковать)
        self.left_key = left_key  # Сохраняем клавишу для движения влево
        self.right_key = right_key  # Сохраняем клавишу для движения вправо
        self.attack_key = attack_key  # Сохраняем клавишу для атаки
    
    def update(self, keys, enemy):  # Метод обновления состояния бойца (вызывается каждый кадр)
        # Кулдаун
        if self.attack_cooldown > 0:  # Если счетчик перезарядки больше 0
            self.attack_cooldown -= 1  # Уменьшаем его на 1 (отсчитываем кадры до возможности новой атаки)
        
        # Движение
        if keys[self.left_key] and self.x > 0:  # Если нажата клавиша влево И боец не у левого края
            self.x -= self.speed  # Сдвигаем бойца влево на величину скорости
        if keys[self.right_key] and self.x < WIDTH - self.width:  # Если нажата клавиша вправо И боец не у правого края
            self.x += self.speed  # Сдвигаем бойца вправо на величину скорости
        
        # Атака
        if keys[self.attack_key] and self.attack_cooldown == 0:  # Если нажата клавиша атаки И перезарядка закончилась
            self.attack_cooldown = 30  # Устанавливаем перезарядку на 30 кадров (0.5 секунды при 60 FPS)
            # Создаем зону атаки
            if self.x < enemy.x:  # Если наш боец левее противника (атакуем вправо)
                attack_zone = pygame.Rect(self.x + self.width, self.y, 50, self.height)  # Создаем прямоугольник атаки справа от бойца (50 пикселей шириной)
            else:  # Если наш боец правее противника (атакуем влево)
                attack_zone = pygame.Rect(self.x - 50, self.y, 50, self.height)  # Создаем прямоугольник атаки слева от бойца
            
            # Проверяем попадание
            if attack_zone.colliderect(pygame.Rect(enemy.x, enemy.y, enemy.width, enemy.height)):  # Если зона атаки пересекается с телом противника
                enemy.health -= 10  # Уменьшаем здоровье противника на 10 единиц
                if enemy.health < 0:  # Если здоровье упало ниже 0
                    enemy.health = 0  # Устанавливаем здоровье в 0 (не может быть отрицательным)
    
    def draw(self, screen):  # Метод отрисовки бойца на экране
        # Рисуем бойца
        pygame.draw.rect(screen, self.color, (self.x, self.y, self.width, self.height))  # Рисуем прямоугольник (тело бойца) с заданными координатами, размером и цветом
        # Полоска здоровья
        pygame.draw.rect(screen, (255,0,0), (self.x, self.y - 30, 100, 15))  # Рисуем красную полоску (фон здоровья) над головой бойца
        pygame.draw.rect(screen, (0,255,0), (self.x, self.y - 30, self.health, 15))  # Рисуем зеленую полоску (текущее здоровье), ширина зависит от значения health

# Создаем бойцов
player1 = Fighter(200, (0, 100, 255), pygame.K_a, pygame.K_s, pygame.K_d)  # Создаем первого бойца: позиция 200, синий цвет (0,100,255), управление: A-влево, D-вправо, F-атака
player2 = Fighter(900, (255, 100, 0), pygame.K_LEFT, pygame.K_RIGHT, pygame.K_SLASH)  # Создаем второго бойца: позиция 900, оранжевый цвет (255,100,0), управление: стрелки влево/вправо, /-атака

# Игровой цикл
running = True  # Устанавливаем флаг работы игры в True (игра активна)
while running:  # Запускаем бесконечный цикл, пока running = True
    # События
    for event in pygame.event.get():  # Получаем все события (нажатия клавиш, закрытие окна и т.д.)
        if event.type == pygame.QUIT:  # Если событие - закрытие окна
            running = False  # Устанавливаем флаг running в False, чтобы выйти из цикла
    
    keys = pygame.key.get_pressed()  # Получаем список всех нажатых в данный момент клавиш
    
    # Обновление
    player1.update(keys, player2)  # Вызываем метод update для первого бойца, передаем нажатые клавиши и противника
    player2.update(keys, player1)  # Вызываем метод update для второго бойца, передаем нажатые клавиши и противника
    
    # Проверка победы
    if player1.health <= 0 or player2.health <= 0:  # Если у любого бойца здоровье закончилось
        print("Game Over!")  # Выводим сообщение в консоль
        running = False  # Завершаем игровой цикл
    
    # Отрисовка
    screen.fill((0,0,0))  # Заливаем экран черным цветом (очищаем от предыдущего кадра)
    player1.draw(screen)  # Вызываем метод отрисовки первого бойца, передаем экран
    player2.draw(screen)  # Вызываем метод отрисовки второго бойца, передаем экран
    pygame.display.flip()  # Обновляем экран (показываем всё, что нарисовали)
    clock.tick(60)  # Ограничиваем FPS до 60 кадров в секунду (игра будет работать с одинаковой скоростью)

pygame.quit()  # Корректно завершаем работу pygame
sys.exit()  # Выходим из программы